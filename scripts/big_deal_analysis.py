#!/usr/bin/env python3
"""大客户销售方案分析"""

def analyze_big_deals():
    target = 7200000  # ¥720万
    
    print("=== 1-2个大客户实现100万美元销售 ===\n")
    
    # 方案1: 单个超大客户
    print("【方案1】单个超大客户")
    print(f"  客户规模: ¥{target:,}")
    print(f"  典型场景:")
    print(f"    - 政府/央企数字化项目")
    print(f"    - 大型集团IT系统集成")
    print(f"    - 跨国企业全国部署")
    print(f"  优势: 一单达成, 效率最高")
    print(f"  风险: 单点依赖, 周期长(6-12月)")
    
    # 方案2: 两个大客户
    print("\n【方案2】两个大客户")
    per_client = target / 2
    print(f"  单客户: ¥{per_client:,}")
    print(f"  典型场景:")
    print(f"    - 省级政府项目 × 2")
    print(f"    - 大型企业集团 × 2")
    print(f"    - 行业头部客户 × 2")
    print(f"  优势: 风险分散, 更可控")
    print(f"  风险: 需要2个大单, 难度中等")
    
    # 关键成功因素
    print("\n=== 关键成功因素 ===")
    factors = [
        "1. 客户画像: 年营收10亿+, IT预算5000万+",
        "2. 决策链条: 需触达VP/CTO/CIO级别",
        "3. 销售周期: 6-12个月 (立项→招标→签约)",
        "4. 解决方案: 定制化, 需技术团队深度参与",
        "5. 竞争壁垒: 技术优势/行业经验/成功案例",
        "6. 商务能力: 招投标/合同谈判/付款条件",
        "7. 交付能力: 项目管理/实施团队/售后支持",
        "8. 关系维护: 长期跟进, 多层级关系网"
    ]
    for f in factors:
        print(f"  {f}")
    
    # 时间线
    print("\n=== 典型时间线 ===")
    timeline = [
        ("月1-2", "客户开发", "识别目标客户, 建立初步联系"),
        ("月3-4", "需求挖掘", "深入了解需求, 方案设计"),
        ("月5-6", "方案演示", "技术交流, 高层汇报"),
        ("月7-8", "商务谈判", "报价, 合同条款"),
        ("月9-10", "招标流程", "投标, 评审, 中标"),
        ("月11-12", "合同签订", "签约, 首付款")
    ]
    for month, phase, action in timeline:
        print(f"  {month}: {phase} - {action}")
    
    # 风险评估
    print("\n=== 风险评估 ===")
    risks = [
        ("高", "单点依赖", "建议同时跟进3-5个大客户"),
        ("高", "周期长", "需要现金流支撑6-12月"),
        ("中", "竞争激烈", "需要差异化优势"),
        ("中", "决策复杂", "需要多部门协调"),
        ("低", "技术门槛", "确保交付能力")
    ]
    for level, risk, mitigation in risks:
        print(f"  [{level}] {risk}: {mitigation}")
    
    # 可行性分析
    print("\n=== 可行性分析 ===")
    print("  ✅ 可行条件:")
    print("    - 有成功案例/行业经验")
    print("    - 有技术壁垒/核心优势")
    print("    - 有高层关系/渠道资源")
    print("    - 有交付团队/售后能力")
    print("    - 有现金流支撑长周期")
    print("\n  ⚠️  挑战:")
    print("    - 大客户决策周期长")
    print("    - 竞争对手实力强")
    print("    - 需要定制化方案")
    print("    - 回款周期可能延长")
    
    # 建议策略
    print("\n=== 建议策略 ===")
    print("  【混合策略】更稳健:")
    print("    - 1个超大客户 (¥400万)")
    print("    - 2个大客户 (¥150万 × 2)")
    print("    - 10个中客户 (¥20万 × 10)")
    print("    总计: ¥900万 (125%达成)")
    print("\n  优势: 风险分散 + 现金流平衡")

if __name__ == "__main__":
    analyze_big_deals()
